create database alumnos;
use alumnos;
CREATE TABLE PROFESORES (
NUMPRO INTEGER(3),
NOMPRO VARCHAR(25),
FNAPRO DATE,
JEFPRO INTEGER(3),
FINPRO DATE,
SALPRO DECIMAL(7,2),
COMPRO DECIMAL(7,2),
ESPPRO VARCHAR(25),
PRIMARY KEY (NUMPRO),
FOREIGN KEY (JEFPRO)REFERENCES PROFESORES(NUMPRO)
);
CREATE TABLE CURSOS(
NUMCUR INTEGER(3),
TITCUR VARCHAR(45),
PRECUR DECIMAL(7,2),
EDICUR INTEGER(3),
HORCUR INTEGER(3),
FINCUR DATE,
FFICUR DATE,
CRECUR INTEGER(3),
NUMPRO INTEGER(3),
PRIMARY KEY (NUMCUR),
FOREIGN KEY (NUMPRO) REFERENCES PROFESORES(NUMPRO)
);
CREATE TABLE TIPOS_SALARIO(
TIPOSAL VARCHAR(20),
INFERIOR INTEGER(4),
SUPERIOR INTEGER(4)
);
CREATE TABLE ALUMNOS(
NUMALU INTEGER(3),
NOMALU VARCHAR(25),
DIRALU VARCHAR(45),
CPOALU VARCHAR (5),
TELALU INTEGER(9),
EMAALU VARCHAR(25),
PRIMARY KEY (NUMALU)
);
CREATE TABLE MATRICULADO(
NUMCUR INTEGER(3),
NUMALU INTEGER(3),
CALIFIC DECIMAL(3,1),
PRIMARY KEY (NUMCUR, NUMALU),
FOREIGN KEY (NUMCUR) REFERENCES CURSOS (NUMCUR),
FOREIGN KEY (NUMALU) REFERENCES ALUMNOS(NUMALU)
);
